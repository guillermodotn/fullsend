func TestSave_RoundTrip(t *testing.T) {
	lf := &LockFile{
		Version:     1,
		GeneratedAt: testTime,
		Harnesses: map[string]HarnessLock{
			"code": {
				Source:     "harness/code.yaml",
				SHA256:     "abc123",
				ResolvedAt: testTime,
				Dependencies: []DependencyEntry{
					{
						Field:     "agent",
						URL:       "https://example.com/agents/code.md",
						SHA256:    "def456",
						FetchedAt: testTime,
					},
					{
						Field:     "skills[0]",
						URL:       "https://example.com/skills/rust/SKILL.md",
						SHA256:    "ghi789",
						FetchedAt: testTime,
						TransitiveDeps: []DependencyEntry{
							{
								Field:     "skills[dep0]",
								URL:       "https://example.com/skills/common/SKILL.md",
								SHA256:    "jkl012",
								FetchedAt: testTime,
							},
						},
					},
				},
			},
		},
	}

	path := filepath.Join(t.TempDir(), ".fullsend", "lock.yaml")
	require.NoError(t, Save(path, lf))

	loaded, err := Load(path)
	require.NoError(t, err)
	require.NotNil(t, loaded)

	assert.Equal(t, 1, loaded.Version)
	assert.Equal(t, testTime, loaded.GeneratedAt)

	hl := loaded.Lookup("code")
	require.NotNil(t, hl)
	assert.Equal(t, "harness/code.yaml", hl.Source)
	assert.Equal(t, "abc123", hl.SHA256)
	require.Len(t, hl.Dependencies, 2)

	assert.Equal(t, "agent", hl.Dependencies[0].Field)
	assert.Equal(t, "https://example.com/agents/code.md", hl.Dependencies[0].URL)

	skill := hl.Dependencies[1]
	assert.Equal(t, "skills[0]", skill.Field)
	require.Len(t, skill.TransitiveDeps, 1)
	assert.Equal(t, "https://example.com/skills/common/SKILL.md", skill.TransitiveDeps[0].URL)
}

