var testTime = time.Date(2026, 6, 8, 12, 0, 0, 0, time.UTC)

func TestLoad_ValidLockFile(t *testing.T) {
	content := `version: 1
generated_at: 2026-06-08T12:00:00Z
harnesses:
  code:
    source: harness/code.yaml
    sha256: abc123def456abc123def456abc123def456abc123def456abc123def456abcd
    resolved_at: 2026-06-08T12:00:00Z
    dependencies:
      - field: agent
        url: https://github.com/fullsend-ai/library/agents/code.md
        sha256: def456abc123def456abc123def456abc123def456abc123def456abc123defg
        fetched_at: 2026-06-08T11:59:55Z
`
	path := filepath.Join(t.TempDir(), "lock.yaml")
	require.NoError(t, os.WriteFile(path, []byte(content), 0o644))

	lf, err := Load(path)
	require.NoError(t, err)
	require.NotNil(t, lf)

	assert.Equal(t, 1, lf.Version)
	assert.Equal(t, testTime, lf.GeneratedAt)

	hl := lf.Lookup("code")
	require.NotNil(t, hl)
	assert.Equal(t, "harness/code.yaml", hl.Source)
	require.Len(t, hl.Dependencies, 1)
	assert.Equal(t, "agent", hl.Dependencies[0].Field)
	assert.Equal(t, "https://github.com/fullsend-ai/library/agents/code.md", hl.Dependencies[0].URL)
}

