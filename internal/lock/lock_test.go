func TestLoad_TransitiveDeps(t *testing.T) {
	content := `version: 1
generated_at: 2026-06-08T12:00:00Z
harnesses:
  code:
    source: harness/code.yaml
    sha256: abc
    resolved_at: 2026-06-08T12:00:00Z
    dependencies:
      - field: skills[0]
        url: https://example.com/skills/rust/SKILL.md
        sha256: def
        fetched_at: 2026-06-08T12:00:00Z
        transitive_deps:
          - field: skills[dep0]
            url: https://example.com/skills/common/SKILL.md
            sha256: ghi
            fetched_at: 2026-06-08T12:00:00Z
`
	path := filepath.Join(t.TempDir(), "lock.yaml")
	require.NoError(t, os.WriteFile(path, []byte(content), 0o644))

	lf, err := Load(path)
	require.NoError(t, err)

	hl := lf.Lookup("code")
	require.NotNil(t, hl)
	require.Len(t, hl.Dependencies, 1)
	require.Len(t, hl.Dependencies[0].TransitiveDeps, 1)
	assert.Equal(t, "https://example.com/skills/common/SKILL.md", hl.Dependencies[0].TransitiveDeps[0].URL)
}

