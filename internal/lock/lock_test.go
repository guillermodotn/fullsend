func TestLookupDep_MaxDepth(t *testing.T) {
	target := "https://example.com/deep-target"
	leaf := DependencyEntry{URL: target, SHA256: "leaf"}
	for i := 0; i < maxLookupDepth+5; i++ {
		leaf = DependencyEntry{
			URL:            fmt.Sprintf("https://example.com/level-%d", i),
			SHA256:         fmt.Sprintf("hash-%d", i),
			TransitiveDeps: []DependencyEntry{leaf},
		}
	}

	hl := &HarnessLock{Dependencies: []DependencyEntry{leaf}}
	assert.Nil(t, hl.LookupDep(target), "should return nil for deps beyond maxLookupDepth")
}

